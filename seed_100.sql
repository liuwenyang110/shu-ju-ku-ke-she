-- 给每个学生随机分配 2~4 门课的成绩（Trigger 会自动算绩点）
-- 用存储过程批量生成
DELIMITER //
CREATE PROCEDURE seed_grades()
BEGIN
  DECLARE i INT DEFAULT 1;
  DECLARE max_stu INT;
  DECLARE max_crs INT;
  DECLARE c INT;
  DECLARE j INT;
  DECLARE rand_score INT;
  DECLARE rand_course INT;

  SELECT MAX(id) INTO max_stu FROM students;
  SELECT MAX(id) INTO max_crs FROM courses;

  WHILE i <= max_stu DO
    -- 每个学生选 2~4 门课
    SET c = 2 + FLOOR(RAND() * 3);
    SET j = 1;
    WHILE j <= c DO
      SET rand_course = 1 + FLOOR(RAND() * max_crs);
      SET rand_score = 45 + FLOOR(RAND() * 56);  -- 45~100 之间

      INSERT IGNORE INTO grades (student_id, course_id, score, status)
      VALUES (i, rand_course, rand_score, 1);

      SET j = j + 1;
    END WHILE;
    SET i = i + 1;
  END WHILE;
END //
DELIMITER ;

CALL seed_grades();
DROP PROCEDURE seed_grades;
